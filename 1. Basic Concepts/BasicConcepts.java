public class BasicConcepts{
    public static void main(String[]args){
        System.out.println("I am a sinmple text");              // prints: I am a simple text
        System.out.println("Waqar"+" Ali "+"Siyal");            // prints: Waqar Ali Siyal
        System.out.println("4"+"5");                            // prints: 45
        System.out.println(8);                                  // prints: 8
        System.out.println(4+5);                                // prints: 9
        System.out.println(4.5f);                               // prints: 4.5
        System.out.println(10.0);                               // prints: 10.0
        System.out.println("8.5"+10.0);                         // prints: 8.510.0
        System.out.println("12.5"+7.2f);                        // prints: 12.57.2
        System.out.println(3.5+4);                              // prints: 7.5
        System.out.println(3.5*2);                              // prints: 7.0
        System.out.println(12/3);                               // prints: 4
        
    }
}